fn print_hex(label: []const u8, data: []const u8) void {
    _ = sys.write(2, label.ptr, label.len);
    _ = sys.write(2, ": ", 2);
    for (data) |b| {
        var hex: [2]u8 = undefined;
        hex[0] = @as(u8, if (b>>4 < 10) '0' + (b>>4) else 'a' + (b>>4) - 10);
